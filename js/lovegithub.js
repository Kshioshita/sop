document.getElementById("repo").addEventListener("mouseover", repo);
document.getElementById("repo").addEventListener("mouseleave", repor);
document.getElementById("repod").addEventListener("mouseover", repo);
document.getElementById("repod").addEventListener("mouseleave", repor);
function repo(){
	var one=document.getElementById("repo");
	one.style.backgroundColor="#ffd6f6";
	var one=document.getElementById("repod");
	one.style.backgroundColor="#ffd6f6"; //"#ffb7e3";
}

function repor(){
	var one=document.getElementById("repod");
		one.style.backgroundColor="transparent";
		var one=document.getElementById("repo");
		one.style.backgroundColor="transparent";
}

